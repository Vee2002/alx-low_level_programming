/**                                                                                                                             

 * largest_number - function returns the largest of 3 numbers                                                                   

 * @a: first parameter passed                                                                                                   

 * @b: second parameter passed                                                                                                  

 * @c: parameter passed                                                                                                         

 * Return: largest number                                                                                                       

 */                                                                                                                             

int largest_number(int a, int b, int c)                                                                                         

{                                                                                                                               

                                                                                                                                

if (a > b && a > c)                                                                                                             

{                                                                                                                               

return (a);                                                                                                                     

}                                                                                                                               

else if (b > a && b > c)                                                                                                        

{                                                                                                                               

return (b);                                                                                                                     

}                                                                                                                               

else                                                                                                                            

{                                                                                                                               

return (c);                                                                                                                     

}                                                                                                                               

}               
